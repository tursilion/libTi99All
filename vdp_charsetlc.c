void charsetlc() {
	vdpmemcpy(gPattern+0x300, TrueLowerCase, sizeof(TrueLowerCase));

#ifdef TI99
	gplvdp(0x0018, gPattern+0x100-1, 64);	// the rest of the character set (-1 to shift it up to match lowercase)
#endif

#ifdef COLECO
#define COLECO_FONT (unsigned char*)0x15A3
	vdpmemcpy(gPattern+0x100, COLECO_FONT, 64*8);	// the rest of the character set (Coleco font isn't shifted like TI's)
#endif

#ifdef GBA
    // TODO: the GBA doesn't apparently have a ROM font, we need to provide it
    // for now I'll be very evil and copy the LC font in, but that leaves out all the numbers and punctuation
    vdpmemcpy(gPattern+0x200, TrueLowerCase, sizeof(TrueLowerCase));
#endif

#ifdef CLASSIC99
    vdpmemcpy(gPattern+0x100, CHARA1, 95*8);      // full set
#endif

	vdpmemset(gPattern+(30*8), 0xfc, 8);	// cursor
}
